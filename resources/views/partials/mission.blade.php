      </div>
    </div>
    <div class="row">
      <div class="col-md-10 col-md-offset-1">
        <p class="text-center">This is during the early humble beginnings of competitve e-sports in the country when satisfactory internet connections for gaming were still not available. This gave rise to (LAN) gaming-cafe culture in Bangladesh. Under these circumstances the first ever known DotA game was hosted on Bangladeshi soil in the mid 2000.</p>
        <p class="text-center">DGL has existed in many forms. We started out as a online forum, but quickly expanded to become a respected e-sports organizational body. We have organized various tournaments, LAN parties and other events in that time. We have volunteered at national gaming events such as the World Cyber Games (WCG) Bangladesh, NSU tournaments (including first one) and various others. We have also sponsored various gamers and teams in national and international competitions, such include various (WCG /WCG Asia) tournaments.</p>
        <p class="text-center">Internet connections have improved, there are now big sponsored e-sports tournaments around the country. But the multiplayer scene is in shambles. Though the community is larger it lacks the organization and regular competitive opportunity it need to grow. That is where we come in as we have done in the past.</p>
